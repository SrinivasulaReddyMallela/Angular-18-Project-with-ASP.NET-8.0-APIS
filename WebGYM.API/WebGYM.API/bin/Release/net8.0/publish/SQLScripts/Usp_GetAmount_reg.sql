CREATE OR ALTER PROCEDURE dbo.Usp_GetAmount_reg
(
	@PlanID		INT,
	@SchemeID	INT
)
AS
BEGIN
	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED; 
	SET QUOTED_IDENTIFIER OFF      
	SET NOCOUNT ON  
	SELECT PlanAmount FROM PlanMaster WHERE PLANID =@PlanID AND SchemeID= @SchemeID

	SET QUOTED_IDENTIFIER ON      
	SET NOCOUNT OFF
END


  